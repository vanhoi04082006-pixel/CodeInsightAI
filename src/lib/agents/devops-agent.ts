function buildDockerfile(p: ProjectInfo): string {
  const lang = (p.language ?? "").toLowerCase();
  const fw = (p.framework ?? "").toLowerCase();
  if (fw === "nextjs" || fw === "next.js") return dockerfileNextJs(p);
  if (lang === "typescript" || lang === "javascript") return dockerfileNodeJs(p);
  if (lang === "python") return dockerfilePython(p);
  if (lang === "go") return dockerfileGo(p);
  if (lang === "rust") return dockerfileRust(p);
  if (lang === "java") return dockerfileJava(p);
  return dockerfileGeneric(p);
}

