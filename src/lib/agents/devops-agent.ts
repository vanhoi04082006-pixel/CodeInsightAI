// ────────────────────────────────────────────────────────────────────────────
// AI customization — if a provider is supplied, optionally enhance the
// generated config with project-specific tweaks (e.g., extra ENV vars,
// framework-aware optimizations). Falls back gracefully on AI failure.
// ────────────────────────────────────────────────────────────────────────────

interface AIEnhancement {
  notes?: string[];
  envVars?: Array<{ key: string; value: string }>;
  warnings?: string[];
}

async function aiEnhance(
  baseContent: string,
  operation: DevOpsOperation,
  projectInfo: ProjectInfo,
  provider: AIProviderConfig,
): Promise<AIEnhancement | null> {
  try {
    const systemMsg =
      "You are a senior DevOps engineer. Given a generated config file and project info, " +
      "return a JSON object with optional `notes` (string[]), `envVars` ({key,value}[]), and " +
      "`warnings` (string[]) to enhance the config. Keep it focused and minimal.";
    const userMsg = `Project: ${JSON.stringify(projectInfo)}\nOperation: ${operation}\n\nGenerated config:\n\n${baseContent.slice(0, 4000)}`;
    return await callAIForJSON<AIEnhancement>(provider, [
      { role: "system", content: systemMsg },
      { role: "user", content: userMsg },
    ]);
  } catch {
    return null;
  }
}

// ────────────────────────────────────────────────────────────────────────────
// Core helper — exported for direct use.
// ────────────────────────────────────────────────────────────────────────────

export async function generateDevOpsConfig(
  operation: DevOpsOperation,
  projectInfo: ProjectInfo,
  provider?: AIProviderConfig,
): Promise<DevOpsConfigResult> {
  // Validate
  if (!OPERATION_META[operation]) {
    throw new Error(`Unknown DevOps operation: ${operation}`);
  }
  if (!projectInfo || !projectInfo.name) {
    throw new Error("projectInfo.name is required");
  }

  const meta = OPERATION_META[operation];

  // Build base content from templates
  let content: string;
  switch (operation) {
    case "dockerize":
      content = buildDockerfile(projectInfo);
      break;
    case "compose":
      content = buildDockerCompose(projectInfo);
      break;
    case "nginx":
      content = buildNginx(projectInfo);
      break;
    case "ci-github-actions":
      content = buildGitHubActionsCI(projectInfo);
      break;
    case "deploy-vercel":
      content = buildVercelConfig(projectInfo);
      break;
    case "deploy-railway":
      content = buildRailwayConfig(projectInfo);
      break;
    case "deploy-render":
      content = buildRenderConfig(projectInfo);
      break;
    case "deploy-fly":
      content = buildFlyConfig(projectInfo);
      break;
    case "deploy-coolify":
      content = buildCoolifyConfig(projectInfo);
      break;
    case "kubernetes-manifest":
      content = buildK8sManifest(projectInfo);
      break;
    default:
      throw new Error(`Operation ${operation} has no template generator`);
  }

  // Optional AI enhancement
  const enhancement: AIEnhancement | null = provider
    ? await aiEnhance(content, operation, projectInfo, provider)
    : null;

  if (enhancement) {
    const banner = [
      enhancement.notes && enhancement.notes.length
        ? `# AI Notes:\n${enhancement.notes.map((n) => `#   - ${n}`).join("\n")}`
        : null,
      enhancement.warnings && enhancement.warnings.length
        ? `# AI Warnings:\n${enhancement.warnings.map((w) => `#   ! ${w}`).join("\n")}`
        : null,
    ]
      .filter(Boolean)
      .join("\n");

    if (banner && (meta.language === "yaml" || meta.language === "dockerfile" || meta.language === "toml")) {
      content = `${banner}\n${content}`;
    } else if (banner && meta.language === "json") {
      // For JSON, fold notes into a "_comment" field instead of a banner.
      try {
        const parsed = JSON.parse(content);
        parsed["_comment"] = (enhancement.notes ?? []).join("; ");
        content = JSON.stringify(parsed, null, 2) + "\n";
      } catch {
        // Leave content as-is if it isn't valid JSON.
      }
    }
  }

  return {
    operation,
    path: meta.path,
    language: meta.language,
    content,
    meta: {
      label: meta.label,
      projectInfo,
      enhancement: enhancement ?? undefined,
    },
  };
}

// ────────────────────────────────────────────────────────────────────────────
// DevOps Agent — extends BaseAgent, handles TaskKind "devops"
// ────────────────────────────────────────────────────────────────────────────

export class DevOpsAgent extends BaseAgent {
  readonly id = "devops-agent" as const;
  readonly info: AgentInfo = {
    id: "devops-agent",
    name: "DevOps Agent",
    description:
      "Generates DevOps and deployment configuration: Docker, docker-compose, nginx, " +
      "CI/CD, and deployment manifests for Vercel, Railway, Render, Fly.io, Coolify, " +
      "and Kubernetes.",
    capabilities: [
      {
        kind: "devops",
        description:
          "Generate Dockerfile, docker-compose, nginx, CI workflows, and deployment manifests",
      },
    ],
    icon: "Server",
    color: "#fb923c",
  };

  protected async execute(
    task: Task,
    signal: AbortSignal,
    onProgress: (p: number, msg: string) => void,
  ): Promise<TaskResult> {
    // 10% — validate
    onProgress(10, "Validating operation and project info");
    const operation = task.input?.operation as DevOpsOperation | undefined;
    const projectInfo = task.input?.projectInfo as ProjectInfo | undefined;
    const provider = task.input?.provider as AIProviderConfig | undefined;

    if (!operation) {
      return this.fail("`operation` is required in task.input");
    }
    if (!projectInfo || !projectInfo.name) {
      return this.fail("`projectInfo.name` is required in task.input");
    }
    if (!OPERATION_META[operation]) {
      return this.fail(`Unsupported operation: ${operation}`);
    }

    if (signal.aborted) throw new Error("Task aborted before generation");

    // 30% — build config
    onProgress(30, `Building ${OPERATION_META[operation].label} for ${projectInfo.name}`);
    const config = await generateDevOpsConfig(operation, projectInfo, provider);

    if (signal.aborted) throw new Error("Task aborted during generation");

    // 70% — finalize content (already done by generateDevOpsConfig)
    onProgress(70, `Finalizing ${config.path}`);

    // 100% — return result
    onProgress(100, `Generated ${config.path} (${config.content.length} bytes)`);

    const artifact: TaskArtifact = {
      kind: "file",
      path: config.path,
      content: config.content,
      language: config.language,
      meta: {
        operation: config.operation,
        label: config.meta.label,
        enhancement: config.meta.enhancement,
        bytes: config.content.length,
      },
    };

    return {
      success: true,
      data: config,
      summary: `Generated ${config.path} for ${projectInfo.name} (operation: ${operation})`,
      artifacts: [artifact],
      metrics: {
        bytes: config.content.length,
        lines: config.content.split("\n").length,
      },
      followUpTasks: this.suggestFollowups(operation, projectInfo),
    };
  }

  /** Suggest logical follow-up tasks based on the operation just performed. */
  private suggestFollowups(
    operation: DevOpsOperation,
    projectInfo: ProjectInfo,
  ): Partial<Task>[] {
    const followUps: Partial<Task>[] = [];

    if (operation === "dockerize") {
      followUps.push({
        kind: "devops",
        title: `Generate docker-compose for ${projectInfo.name}`,
        priority: "medium",
        input: { operation: "compose", projectInfo },
      });
      followUps.push({
        kind: "devops",
        title: `Generate nginx reverse-proxy config`,
        priority: "low",
        input: { operation: "nginx", projectInfo },
      });
    }
    if (operation === "dockerize" || operation === "compose") {
      followUps.push({
        kind: "devops",
        title: `Generate GitHub Actions CI workflow`,
        priority: "high",
        input: { operation: "ci-github-actions", projectInfo },
      });
    }
    if (operation === "kubernetes-manifest") {
      followUps.push({
        kind: "devops",
        title: `Generate Dockerfile for container build`,
        priority: "high",
        input: { operation: "dockerize", projectInfo },
      });
    }

    return followUps;
  }

  private fail(error: string): TaskResult {
    return {
      success: false,
      data: null,
      summary: `DevOps Agent failed: ${error}`,
      artifacts: [],
    };
  }
}

