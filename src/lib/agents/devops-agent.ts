function detectBaseImage(language: string, framework?: string): string {
  const lang = language.toLowerCase();
  const fw = (framework ?? "").toLowerCase();
  if (lang === "typescript" || lang === "javascript") {
    if (fw === "nextjs" || fw === "next.js") return "node:20-alpine";
    return "node:20-alpine";
  }
  if (lang === "python") {
    if (fw === "django") return "python:3.11-slim";
    return "python:3.11-slim";
  }
  if (lang === "go") return "golang:1.21-alpine";
  if (lang === "rust") return "rust:1.75";
  if (lang === "java") return "eclipse-temurin:21-jdk-alpine";
  if (lang === "ruby") return "ruby:3.2-slim";
  if (lang === "php") return "php:8.2-fpm-alpine";
  return "node:20-alpine";
}

function detectPackageCmd(packageManager: string | undefined): {
  install: string;
  build: string;
  start: string;
  lockfile: string;
} {
  switch ((packageManager ?? "npm").toLowerCase()) {
    case "yarn":
      return {
        install: "yarn install --frozen-lockfile",
        build: "yarn build",
        start: "yarn start",
        lockfile: "yarn.lock",
      };
    case "pnpm":
      return {
        install: "pnpm install --frozen-lockfile",
        build: "pnpm build",
        start: "pnpm start",
        lockfile: "pnpm-lock.yaml",
      };
    case "bun":
      return {
        install: "bun install --frozen-lockfile",
        build: "bun run build",
        start: "bun run start",
        lockfile: "bun.lockb",
      };
    case "npm":
    default:
      return {
        install: "npm ci",
        build: "npm run build",
        start: "npm start",
        lockfile: "package-lock.json",
      };
  }
}

/** Next.js standalone build Dockerfile — multi-stage, non-root, minimal image. */
function dockerfileNextJs(p: ProjectInfo): string {
  const pkg = detectPackageCmd(p.packageManager);
  const port = p.port ?? 3000;
  return `# Multi-stage Dockerfile for Next.js — ${p.name}
# Generated by CodeInsight AI DevOps Agent

# ── deps ───────────────────────────────────────────────────────────────────
FROM node:20-alpine AS deps
WORKDIR /app
COPY package*.json ${pkg.lockfile} ./
RUN ${pkg.install}

# ── builder ─────────────────────────────────────────────────────────────────
FROM node:20-alpine AS builder
WORKDIR /app
COPY --from=deps /app/node_modules ./node_modules
COPY . .
ENV NEXT_TELEMETRY_DISABLED=1
RUN ${pkg.build}

# ── runner ──────────────────────────────────────────────────────────────────
FROM node:20-alpine AS runner
WORKDIR /app
ENV NODE_ENV=production
ENV NEXT_TELEMETRY_DISABLED=1
RUN addgroup -g 1001 -S nodejs && adduser -S nextjs -u 1001
COPY --from=builder /app/public ./public
COPY --from=builder --chown=nextjs:nodejs /app/.next/standalone ./
COPY --from=builder --chown=nextjs:nodejs /app/.next/static ./.next/static
USER nextjs
EXPOSE ${port}
ENV PORT=${port}
ENV HOSTNAME="0.0.0.0"
CMD ["node", "server.js"]
`;
}

