function dockerfilePython(p: ProjectInfo): string {
  const framework = (p.framework ?? "").toLowerCase();
  const entrypoint = p.entrypoint ?? "main:app";
  const port = p.port ?? 8000;
  const startCmd =
    framework === "django"
      ? `gunicorn ${entrypoint.split(":")[0] || "myproject"}.wsgi:application --bind 0.0.0.0:${port}`
      : `uvicorn ${entrypoint}:app --host 0.0.0.0 --port ${port}`;
  return `# Dockerfile for Python — ${p.name}
FROM python:3.11-slim
WORKDIR /app
ENV PYTHONDONTWRITEBYTECODE=1
ENV PYTHONUNBUFFERED=1
COPY requirements.txt .
RUN pip install --no-cache-dir -r requirements.txt
COPY . .
EXPOSE ${port}
CMD ["sh", "-c", "${startCmd}"]
`;
}

function dockerfileGo(p: ProjectInfo): string {
  const port = p.port ?? 8080;
  return `# Multi-stage Dockerfile for Go — ${p.name}
FROM golang:1.21-alpine AS builder
WORKDIR /app
COPY go.mod go.sum ./
RUN go mod download
COPY . .
RUN CGO_ENABLED=0 GOOS=linux go build -ldflags="-w -s" -o /server .

FROM alpine:latest
RUN apk --no-cache add ca-certificates
WORKDIR /root/
COPY --from=builder /server .
EXPOSE ${port}
CMD ["./server"]
`;
}

function dockerfileRust(p: ProjectInfo): string {
  const port = p.port ?? 8080;
  const binName = p.name || "app";
  return `# Multi-stage Dockerfile for Rust — ${p.name}
FROM rust:1.75 AS builder
WORKDIR /app
COPY Cargo.toml Cargo.lock ./
RUN mkdir src && echo "fn main() {}" > src/main.rs && cargo build --release && rm -rf src
COPY src ./src
RUN cargo build --release

FROM debian:bookworm-slim
RUN apt-get update && apt-get install -y --no-install-recommends ca-certificates && rm -rf /var/lib/apt/lists/*
WORKDIR /app
COPY --from=builder /app/target/release/${binName} ./
EXPOSE ${port}
CMD ["./${binName}"]
`;
}

